    private void btnbeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbeliActionPerformed
          // Mendapatkan detail item
        String namaBarang = lblnama.getText();
        int hargaBarang = Integer.parseInt(lblharga.getText());
        int jumlahBarang = Integer.parseInt(lbljumlah.getText());

        // Mendapatkan saldo user
        HomeForm homeForm = (HomeForm) SwingUtilities.getWindowAncestor(this);
        String fullname = homeForm.getFullname();
        int saldoUser = homeForm.getSaldo(fullname);

        // Menampilkan konfirmasi pembelian
        int response = JOptionPane.showConfirmDialog(this, "Ingin membeli " + namaBarang + " dengan harga Rp" + hargaBarang + "?", "Konfirmasi Pembelian", JOptionPane.YES_NO_OPTION);

        if (response == JOptionPane.YES_OPTION) {
            if (saldoUser >= hargaBarang && jumlahBarang > 0) {
                // Mengurangi saldo user
                int newSaldo = saldoUser - hargaBarang;
                homeForm.updateSaldo(fullname, newSaldo);

                // Mengurangi jumlah barang
                int newJumlahBarang = jumlahBarang - 1;
                lbljumlah.setText(String.valueOf(newJumlahBarang));

                // Menambah riwayat transaksi
                homeForm.addTransaksi(fullname, namaBarang, hargaBarang);
                homeForm.updateSaldoLabel(fullname);

                // Menampilkan pesan sukses
                JOptionPane.showMessageDialog(this, "Pembelian berhasil!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Saldo tidak mencukupi atau barang habis.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnbeliActionPerformed

