    public void updateImage() {
        // Memastikan ukuran label diperbarui
        lblgambar.revalidate();

        int labelWidth = lblgambar.getWidth();
        int labelHeight = lblgambar.getHeight();

        if (labelWidth > 0 && labelHeight > 0 && originalIcon != null) {
            // Mengubah ukuran gambar sesuai dengan ukuran label
            Image scaledImage = originalIcon.getImage().getScaledInstance(labelWidth, labelHeight, Image.SCALE_SMOOTH);

            // Mengkonversi Image ke ImageIcon agar bisa ditampilkan di JLabel
            ImageIcon scaledIcon = new ImageIcon(scaledImage);
            lblgambar.setIcon(scaledIcon);
        } else if (originalIcon != null) {
            // Jika ukuran label belum siap, gunakan ukuran default dari originalIcon
            lblgambar.setIcon(originalIcon);

            // Menggunakan SwingUtilities.invokeLater untuk menunda eksekusi hingga ukuran label siap
            SwingUtilities.invokeLater(this::updateImage);
        }
    }


