    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        String emailTxt = email.getText().trim();
        char[] passChars = password.getPassword();
        String pass = new String(passChars).trim();

        if (!emailTxt.isEmpty() && passChars.length > 0) {
            try {
                Connection conn = dbKoneksi.getConnection();
                String query = "SELECT username, role FROM akun WHERE email=? AND password=?";
                PreparedStatement pst = conn.prepareStatement(query);
                pst.setString(1, emailTxt);
                pst.setString(2, pass);

                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    // Login successful, get the username and role
                    String fullName = rs.getString("username");
                    String role = rs.getString("role");
                    JOptionPane.showMessageDialog(this, "Login berhasil!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    // Create and show HomeForm with the user's full name
                    HomeForm homeForm = new HomeForm(fullName);

                    // Check the role of the user
                    if (role.equals("admin")) {
                        homeForm.hidesaldoadmin();
                        homeForm.setVisible(true);
                    } else if (role.equals("user")) {
                        homeForm.hideAdminDashboard(); // Hide admin dashboard for normal users
                        homeForm.setVisible(true);
                    }

                    this.dispose(); // Close the login form
                } else {
                    // Login failed
                    JOptionPane.showMessageDialog(this, "Username atau password salah!", "Error", JOptionPane.ERROR_MESSAGE);
                }
                conn.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } else {
            System.out.println("Please fill in all fields.");
        }
    }//GEN-LAST:event_LoginBtnActionPerformed

